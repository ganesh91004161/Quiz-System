        let score = 0;
        let currentQuestionIndex = 0;
        const quizContainer = document.getElementById("quiz");
        const scoreDisplay = document.getElementById("score");
        const nextButton = document.getElementById("next-btn");
        const welcomeContainer = document.getElementById("welcome-container");
        const quizContent = document.getElementById("quiz-container");
        function startQuiz() {
            welcomeContainer.style.display = "none";
            quizContent.style.display = "block";
            loadQuestion();
        }
        function loadQuestion() {
            quizContainer.innerHTML = "";
            nextButton.style.display = "none";
            const item = quizData[currentQuestionIndex];
            const questionDiv = document.createElement("div");
            questionDiv.classList.add("question");
            questionDiv.textContent = `${currentQuestionIndex + 1}. ${item.question}`;
            quizContainer.appendChild(questionDiv);
            const optionsDiv = document.createElement("div");
            optionsDiv.classList.add("options");
            item.options.forEach(option => {
                const button = document.createElement("button");
                button.textContent = option;
                button.onclick = () => checkAnswer(button, option, item.answer);
                optionsDiv.appendChild(button);
            });
            quizContainer.appendChild(optionsDiv);
        }
        function checkAnswer(button, selected, correct) {
            const feedback = document.createElement("div");
            feedback.classList.add("feedback");
            feedback.style.opacity = "0";
            setTimeout(() => {
                feedback.style.opacity = "1";
                if (selected === correct) {
                    feedback.textContent = "Correct!";
                    feedback.style.color = "green";
                    score += 1;
                } else {
                    feedback.textContent = "Try Again!";
                    feedback.style.color = "red";
                }
                scoreDisplay.textContent = score;
                nextButton.style.display = "block";
                disableOptions();
            }, 300);
            quizContainer.appendChild(feedback);
        }
        function disableOptions() {
            document.querySelectorAll(".options button").forEach(btn => btn.disabled = true);
        }
        function nextQuestion() {
            currentQuestionIndex++;
            if (currentQuestionIndex < quizData.length) {
                loadQuestion();
            } else {
                quizContainer.innerHTML = `<h3>Quiz Completed!</h3><p>Your final score: ${score}</p>`;
                nextButton.style.display = "none";
            }
        }
      
